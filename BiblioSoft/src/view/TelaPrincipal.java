    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        LivroDAO dao = new LivroDAO();
        EmprestimoDAO daoEmprestimo = new EmprestimoDAO();
        
         ExcluiAlunoFrame confirmarDialog = new ExcluiAlunoFrame(this, rootPaneCheckingEnabled);
         
         if (jtLivro.getSelectedRow() != -1){
             
             if(!daoEmprestimo.livroEmprestado(Integer.parseInt(jtLivro.getValueAt(jtLivro.getSelectedRow(), 0).toString()))){
                confirmarDialog.setVisible(true);
                if (confirmarDialog.getConfirmar()){
                    DefaultTableModel dtmLivros = (DefaultTableModel) jtLivro.getModel();
                    int id = (int)dtmLivros.getValueAt(jtLivro.getSelectedRow(), 0);
                    dao.deletar(id);
                    dtmLivros.removeRow(jtLivro.getSelectedRow());
                } 
             }else{
                 aviso("Você não pode excluir um livro registrado no sistema de emprestimo!");
             }
             
         }else {
             aviso("Selecione um registro na tabela!");
         }
    }//GEN-LAST:event_jButton9ActionPerformed

