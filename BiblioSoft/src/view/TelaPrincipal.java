    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        EmprestimoDAO dao = new EmprestimoDAO();
        Emprestimo emprestimo = new Emprestimo();
        
        
        if (!dao.livroEmprestado(Integer.parseInt(jtLivrosEmprestimo.getValueAt(jtLivrosEmprestimo.getSelectedRow(), 0).toString()) )){
            LivroDAO daoLivro = new LivroDAO();
            Livro livro = new Livro();
        
            livro.setId_livro(Integer.parseInt(emprestimoID_livro.getText()));
            livro.setDisponibilidade("Indisponível");
        
            daoLivro.atualizaDisponibilidade(livro);
        
            emprestimo.setData_devolucao(converteData(emprestimoDat_devolucao.getText()));
            emprestimo.setData_emprestimo(converteData(emprestimoDat_emprestimo.getText()));
            emprestimo.setId_aluno(Integer.parseInt(emprestimoID_aluno.getText()));
            emprestimo.setId_livro(Integer.parseInt(emprestimoID_livro.getText()));
        
            dao.adiciona(emprestimo);
            limpaDadosEmprestimo();
        }else {
            aviso("Este livro está indisponível!");
        }
        
    }//GEN-LAST:event_jButton12ActionPerformed

