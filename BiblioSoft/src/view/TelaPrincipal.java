    private void botaoRegistrarMultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRegistrarMultaActionPerformed
        //Mostra JDialog para confirmar o registro da multa
        ConfirmaMulta dialog = new ConfirmaMulta(this, rootPaneCheckingEnabled);
        MultaDAO dao = new MultaDAO();
        
        //Preenche o label do JDialog com as informações
        dialog.setTitulo(multaValor.getText(), dao.getPessoaMultada(Integer.parseInt(multaID.getText().toString())));
        dialog.setVisible(true);
        
        //Verifica se o cliente clicou no botao 'Confirmar'
        if(dialog.getConfirmar()){
            Multa multa = new Multa();
        
            //Cria uma multa
            multa.setId_aluno(Integer.parseInt(multaIDaluno.getText().toString()));
            multa.setDescricao(multaDesc.getSelectedItem().toString());
            multa.setValor(Double.parseDouble(multaValor.getText().toString()));
        
            //Adiciona ao banco
            dao.adiciona(multa);
            //Limpa os campos de entrada com as informações
            limpaDadosMulta();
            //Limpa a tabela de multas
            limpaTabela((DefaultTableModel)jtMultaAluno.getModel());
        }
    }//GEN-LAST:event_botaoRegistrarMultaActionPerformed

    private void jtAlunoMultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtAlunoMultaMouseClicked
        //Verifica se o cliente selecionou uma linha na tabela Aluno
        if (jtAlunoMulta.getSelectedRow() != -1){
            MultaDAO dao = new MultaDAO();
            
            //Lista com as multas no nome do aluno selecionado na tabela Aluno
            List<Multa> multas = dao.pesquisa(jtAlunoMulta.getValueAt(jtAlunoMulta.getSelectedRow(), 1).toString());
            
            DefaultTableModel dtmMulta = (DefaultTableModel) jtMultaAluno.getModel();
            limpaTabela(dtmMulta);
          
            //Lsta os registros da tabela
            for (Multa multa: multas){
                Object [] dados = {multa.getId_multa(), multa.getNome_aluno(), multa.getDescricao(), Double.toString(multa.getValor())};
                dtmMulta.addRow(dados);
            }
            
            //Toda vez que o cliente selecionar algum aluno, os campos de entrada são apagados
            limpaDadosMulta();
            
            //Mostra a divida total do aluno
            multaTotValor.setText(dao.getTotDiv(jtAlunoMulta.getValueAt(jtAlunoMulta.getSelectedRow(), 1).toString()));
            
            
        }
    }//GEN-LAST:event_jtAlunoMultaMouseClicked

